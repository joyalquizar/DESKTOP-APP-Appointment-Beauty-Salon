    private void senddataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_senddataActionPerformed
        String imagePath = null;
        String serviceName = ServiceName.getText().trim();
        String cost = txt_prices.getText().trim();
        String description = txt_description.getText().trim();
        Icon icon = jLimage.getIcon();

        if (icon != null && icon instanceof ImageIcon) {
            ImageIcon imageIcon = (ImageIcon) icon;
            imagePath = imageIcon.getDescription(); // this is the file path
        }

        if (imagePath == null || imagePath.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please upload an image.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int rowsInserted = 0;

        try (FileInputStream fis = new FileInputStream(new File(imagePath))) {
            String query = "INSERT INTO tblservices (ServiceName, Cost, Description, Image) VALUES (?, ?, ?, ?)";
            pst = con.prepareStatement(query);
            pst.setString(1, serviceName);
            pst.setString(2, cost);
            pst.setString(3, description);
            pst.setBinaryStream(4, fis, (int) new File(imagePath).length());

            rowsInserted = pst.executeUpdate();
            pst.close();

            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(this, "Service added successfully!", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                refreshTableServicess(); // your own table refresh

                // ✅ Also refresh ServicesPage JTable
                if (services != null) {
                    services.refreshTableServicess();
                    if (payment != null) {
                    payment.refreshTableServicess();
                }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_senddataActionPerformed

