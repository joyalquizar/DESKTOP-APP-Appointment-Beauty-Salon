    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int selectedRow = TableServicess.getSelectedRow();

if (selectedRow == -1) {
    JOptionPane.showMessageDialog(this, "Please select an item to delete.");
    return;
}

int response = JOptionPane.showConfirmDialog(
        this,
        "Are you sure you want to delete the selected item?",
        "Confirm Delete",
        JOptionPane.OK_CANCEL_OPTION,
        JOptionPane.WARNING_MESSAGE
);

if (response == JOptionPane.OK_OPTION) {
    try {
        // Get the selected service ID from the table (assuming it's in the first column)
        int serviceId = (int) TableServicess.getValueAt(selectedRow, 0);

        // Delete from the database
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/bpmsdb", "root", "");
        String deleteQuery = "DELETE FROM tblservices WHERE ID = ?";
        PreparedStatement pstmt = con.prepareStatement(deleteQuery);
        pstmt.setInt(1, serviceId);

        int rowsAffected = pstmt.executeUpdate();

        if (rowsAffected > 0) {
            // Remove from JTable
            DefaultTableModel model = (DefaultTableModel) TableServicess.getModel();
            model.removeRow(selectedRow);

            // Clear form fields
            ServiceName.setText("");
            txt_prices.setText("");
            txt_description.setText("");
            jLimage.setIcon(null);

            JOptionPane.showMessageDialog(this, "Item deleted successfully.");
        } else {
            JOptionPane.showMessageDialog(this, "Failed to delete item from database.");
        }

        pstmt.close();
        con.close();

    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
    }
}

    }//GEN-LAST:event_jButton1ActionPerformed

