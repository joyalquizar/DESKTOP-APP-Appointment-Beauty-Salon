        public void refreshTableServicess() {
            DefaultTableModel model = (DefaultTableModel) TableServicess.getModel();
            model.setRowCount(0);

            try {
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/bpmsdb", "root", "");
                String sql = "SELECT * FROM tblservices";
                System.out.println("con = " + con);
                System.out.println("TableServicess = " + TableServicess);

                PreparedStatement pst = con.prepareStatement(sql);
                ResultSet rs = pst.executeQuery(); // from java.sql.*

                while (rs.next()) {
                    byte[] imageBytes = rs.getBytes("Image");
                    ImageIcon icon = null;
                    if (imageBytes != null) {
                        Image img = Toolkit.getDefaultToolkit().createImage(imageBytes);
                        Image scaledImg = img.getScaledInstance(60, 60, Image.SCALE_SMOOTH);
                        icon = new ImageIcon(scaledImg);
                    }

                    if (con == null) {
                        JOptionPane.showMessageDialog(null, "Database connection is not established.");
                        return;
                    }
                    if (TableServicess == null) {
                        JOptionPane.showMessageDialog(null, "TableServicess is not initialized.");
                        return;
                    }

                    model.addRow(new Object[]{
                        rs.getInt("ID"),
                        rs.getString("ServiceName"),
                        rs.getString("Cost"),
                        rs.getString("Description"),
                        icon
                            
                    });
                }
                

                rs.close();
                pst.close();

            } catch (Exception e) {
                e.printStackTrace(); // 👈 This is important to reveal full error
                JOptionPane.showMessageDialog(null, "Error loading services: " + e.getMessage());
            }

            // Set row height and cell renderer for image column (index 6)
            TableServicess.setRowHeight(100);
            TableServicess.getColumnModel().getColumn(4).setCellRenderer(new DefaultTableCellRenderer() {
                @Override
                public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected,
                    boolean hasFocus, int row, int column) {
                    if (value instanceof ImageIcon) {
                        setIcon((ImageIcon) value);
                        setText("");
                    } else {
                        setIcon(null);
                        setText("No Image");
                    }
                    return this;
                }
            });
            
    }//GEN-LAST:event_jSaveActionPerformed

