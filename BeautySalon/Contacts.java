public void refreshClientNotification() {
    DefaultTableModel model = (DefaultTableModel) ClientNotification.getModel();
    model.setRowCount(0); // Clear table

    try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/bpmsdb", "root", "");
        String sql = "SELECT * FROM clientcontact";
        PreparedStatement pst = con.prepareStatement(sql);
        java.sql.ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
               
                rs.getString("Admin_name"),
                rs.getString("subject"),
                rs.getString("email"),
                rs.getString("message"),
               
            });
        }

        rs.close();
        pst.close();
        con.close();

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Failed to load bookings.");
    }
}
