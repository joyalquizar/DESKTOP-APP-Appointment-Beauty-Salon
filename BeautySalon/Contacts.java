    private void ClientNotificationMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ClientNotificationMouseClicked
        int selectedRow = 0;

    if (selectedRow != -1) {
        // Get the selected customer's name
        String Admin_name = ClientNotification.getValueAt(selectedRow, 0).toString();

        // Now retrieve the corresponding data from the database
        try (Connection conn = getConnection()) {
            String sql = "SELECT subject, email, message FROM clientcontact WHERE Admin_name = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, Admin_name);

            java.sql.ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                String subject = rs.getString("Subject");
                String email = rs.getString("Email");
                String message = rs.getString("Message");

                // Open the message frame with retrieved data
                Adminmessage adminmessage = new Adminmessage(Admin_name, subject, email, message);
                adminmessage.setLocationRelativeTo(null);
                adminmessage.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "No message found for this customer.");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
            e.printStackTrace();
        }
    } else {
        JOptionPane.showMessageDialog(this, "Please select a row.");
    }

    }//GEN-LAST:event_ClientNotificationMouseClicked


