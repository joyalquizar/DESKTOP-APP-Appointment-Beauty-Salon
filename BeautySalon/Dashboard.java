public void refreshjTableNotification() {
    DefaultTableModel model = (DefaultTableModel) jTableNotification.getModel();
    model.setRowCount(0); // Clear table

    try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/bpmsdb", "root", "");
        String sql = "SELECT * FROM contact";
        PreparedStatement pst = con.prepareStatement(sql);
        java.sql.ResultSet rs = pst.executeQuery();

        while (rs.next()) {
            model.addRow(new Object[]{
               
                rs.getString("name"),
                rs.getString("subject"),
                rs.getString("email"),
                rs.getString("message"),
               
            });
        }

        rs.close();
        pst.close();
        con.close();

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Failed to load bookings.");
    }
}
   

    public void loadjTableNotification() {
    try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/bpmsdb", "root", "");
        String sql = "SELECT customer_name FROM contact ORDER BY id DESC";
        PreparedStatement pst = con.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        DefaultTableModel model = (DefaultTableModel) jTableNotification.getModel();
        model.setRowCount(0);

        
        while (rs.next()) {
            String name = rs.getString("customer_name");
            model.addRow(new Object[]{name});
        }

        con.close();
    } catch (Exception e) {
        e.printStackTrace();
    }
}


