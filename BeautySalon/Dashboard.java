    private void jTableNotificationMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableNotificationMouseClicked
                                                     
    int selectedRow = jTableNotification.getSelectedRow();

    if (selectedRow != -1) {
        // Get the selected customer's name
        String customer_name = jTableNotification.getValueAt(selectedRow, 0).toString();

        // Now retrieve the corresponding data from the database
        try (Connection conn = getConnection()) {
            String sql = "SELECT subject, email, message FROM contact WHERE customer_name = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, customer_name);

            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                String subject = rs.getString("Subject");
                String email = rs.getString("Email");
                String message = rs.getString("Message");

                // Open the message frame with retrieved data
                Messeges messege = new Messeges(customer_name, subject, email, message);
                messege.setLocationRelativeTo(null);
                messege.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "No message found for this customer.");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
            e.printStackTrace();
        }
    } else {
        JOptionPane.showMessageDialog(this, "Please select a row.");
    }

        // Open the custom message frame
    }//GEN-LAST:event_jTableNotificationMouseClicked


